package br.com.rpgbasico.personagens;

public class paladino extends geral{
    private String juramento;
    private int escudo;
    private int armadura;
    private int espada;
    private int xpMagia;
    private int golpeDivino;

    public String getJuramento() {
        return juramento;
    }

    public void setJuramento(String juramento) {
        this.juramento = juramento;
    }

    public int getEscudo() {
        return escudo;
    }

    public void setEscudo(int escudo) {
        this.escudo = escudo;
    }

    public int getArmadura() {
        return armadura;
    }

    public void setArmadura(int armadura) {
        this.armadura = armadura;
    }

    public int getEspada() {
        return espada;
    }

    public void setEspada(int espada) {
        this.espada = espada;
    }

    public int getXpMagia() {
        return xpMagia;
    }

    public void setXpMagia(int xpMagia) {
        this.xpMagia = xpMagia;
    }

    public int getGolpeDivino() {
        return golpeDivino;
    }

    public void setGolpeDivino(int golpeDivino) {
        this.golpeDivino = golpeDivino;
    }
}
